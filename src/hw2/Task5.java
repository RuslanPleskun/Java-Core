package src.hw2;

public class Task5 {

    public static void main(String[] args) {
        System.out.println("Task 5");
        task5();
    }

    public static void task5() {
        System.out.println("All the numbers from 1 to 50 in reverse order");
        for (int i = 50; i >= 1; i--) {
            System.out.print(i);
        }
    }

}
